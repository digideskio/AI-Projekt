package sokoban;

/**
 * Small class that stores a position
 */
public class Position
{
    /**
     * The x coordinate for this position
     */
    public int x;
    /**
     * The x coordinate for this position
     */
    public int y;

    /**
     * Create a new position
     * 
     * @param x X coordinate
     * @param y Y coordinate
     */
    public Position(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
}
